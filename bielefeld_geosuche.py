    def show_search_menu(self):
        """Zeigt das Dropdown-Menü zur Auswahl des Suchmodus an.

        Das Menü wird unterhalb der rechten Seite des Sucheingabefeldes
        positioniert.
        """
        #QgsMessageLog.logMessage(
        #    "show_search_menu()",
        #    "bielefeldGeosuche",
        #    Qgis.Info
        #)
        #action_rect = self.line_edit.actionPosition(
        #    self.dropdown_action
        #)
        global_pos = self.line_edit.mapToGlobal(
            self.line_edit.rect().bottomRight()
        )
        self.search_mode_menu.exec_(global_pos)


    def set_search_mode(self, mode):
        """Wechselt den aktiven Suchmodus und passt die Oberfläche an.

        Setzt den Completer zurück, aktualisiert den Haken im Menü und
        stellt Platzhaltertext sowie Eingabe-Modus passend zum gewählten
        Modus ein. Bei Katalog-Suchen (Flurstück/Adresse) wird sofort
        eine erste Anfrage ohne Suchtext gesendet.

        Args:
            mode (str): Der neue Suchmodus ('search', 'parcel' oder 'address').
        """
        #QgsMessageLog.logMessage(
        #    "set_search_mode: " + mode,
        #    "bielefeldGeosuche",
        #    Qgis.Info
        #)
        self.current_search_mode = mode

        #QgsMessageLog.logMessage(
        #    "self.line_edit.text() " + self.line_edit.text(),
        #    "bielefeldGeosuche",
        #    Qgis.Info
        #)

        # Wenn der Suchmodus gewechselt wird, nmüssen die alte Suche und die alten Ergebnisse zurückgesetzt werden
        self.reset_completer()

        #QgsMessageLog.logMessage(
        #    "self.line_edit.text() " + self.line_edit.text(),
        #    "bielefeldGeosuche",
        #    Qgis.Info
        #)
        

        # Menü-Haken aktualisieren
        for action in self.search_mode_menu.actions():
            action.setChecked(
                self.search_modes[action.text()] == mode
            )

        # Placeholder und catalog_step ändern
        if mode == "search":
            self.line_edit.setReadOnly(False)
            self.line_edit.setClearButtonEnabled(True)
            self.line_edit.setPlaceholderText("bielefeldGEOSUCHE")
        elif mode == "parcel":
            self.line_edit.setReadOnly(True)
            self.line_edit.setClearButtonEnabled(False)
            self.line_edit.setPlaceholderText("Katalog-Suche Flurstück")
            self.current_catalog_step = "getgemarkungen"
        elif mode == "address":
            self.line_edit.setReadOnly(True)
            self.line_edit.setClearButtonEnabled(False)
            self.line_edit.setPlaceholderText("Katalog-Suche Adresse")
            self.current_catalog_step = "getbuchstaben"

        # für die Katalog-Suche direkt eine neue Suche starten, da hier schon Ergebnisse ohne Suchtext ausgegeben werden müssen
        if mode == "parcel" or mode == "address":
            self.perform_search()


    def create_text_icon(self, text, font, color):
        """Erzeugt ein QIcon aus einem Text-Zeichen mit gegebener Schrift und Farbe.

        Zeichnet das Zeichen zentriert auf ein transparentes 16x16-Pixel-Pixmap.

        Args:
            text (str): Das darzustellende Zeichen (z. B. ein Unicode-Symbol).
            font (QFont): Die zu verwendende Schriftart.
            color (QColor): Die Farbe des Zeichens.

        Returns:
            QIcon: Das erzeugte Icon.
        """
        size = 16
        pixmap = QPixmap(size, size)
        pixmap.fill(Qt.transparent)

        painter = QPainter(pixmap)
        painter.setRenderHint(QPainter.Antialiasing)
        painter.setPen(color)
        painter.setFont(font)

        fm = QFontMetrics(font)

        text_width = fm.horizontalAdvance(text)
        #text_height = fm.height()

        x = (size - text_width) / 2

        # Optische Vertikal-Zentrierung über Baseline
        y = (size + fm.ascent() - fm.descent()) / 2

        painter.drawText(int(x), int(y), text)
        painter.end()

        return QIcon(pixmap)

    def on_menu_open(self):
        """Wechselt das Dropdown-Symbol zur aktiven Darstellung beim Öffnen des Menüs."""
        self.dropdown_action.setIcon(self.icon_active)

    def on_menu_close(self):
        """Stellt das Dropdown-Symbol auf die normale Darstellung zurück."""
        self.dropdown_action.setIcon(self.icon_normal)

    def on_popup_closed(self):
        """Gibt den Fokus vom Sucheingabefeld zurück an den Kartenausschnitt."""
        #QgsMessageLog.logMessage(
        #    "on_popup_closed()",
        #    "bielefeldGeosuche",
        #    Qgis.Info
        #)
        # Fokus vom LineEdit entfernen
        self.line_edit.clearFocus()

        # Optional: Fokus explizit zurück zur Karte
        self.iface.mapCanvas().setFocus()

    def reset_completer(self):
        """Setzt den Autovervollständiger und alle Suchzustände vollständig zurück.

        Löscht den Suchbegriff, die Gemarkung, den Anfangsbuchstaben und die
        Ergebnisanzahl. Schließt das Popup, leert das Eingabefeld, entfernt
        eine vorhandene RubberBand-Markierung und leert das Datenmodell.
        """
        #QgsMessageLog.logMessage(
        #    "reset_completer()",
        #    "bielefeldGeosuche",
        #    Qgis.Info
        #)
        self.current_search_term = ""
        self.gemarkung = ""
        self.anfangsbuchstabe = ""
        self.last_result_count = 0
        
        # Alte Markierung entfernen
        self.clear_rubber_band()  

        popup = self.completer.popup()

        # Popup schließen
        popup.hide()

        # Selektion entfernen
        popup.setCurrentIndex(popup.model().index(-1, -1))
        self.line_edit.clear()

        # Completion Prefix leeren
        self.completer.setCompletionPrefix("")

        # Modell leeren (optional)
        self.model.setStringList([])